<div class="row">
<div class="col-md-6">

<p>
Con el objetivo de ofrecer mayores condiciones de equidad y de igualdad de 
oportunidades para todos los sustentantes en los procesos de evaluación del 
Servicio Profesional Docente, este sitio ofrece diversos recursos de apoyo para 
el autoestudio y la formación.
</p>
<p>
Los docentes, técnicos docentes, directivos, personal con funciones de 
supervisión y asesoría técnico pedagógica, dispondrán de acceso libre y 
permanente a un simulador de exámenes, a Guías de estudio conforme a cada 
perfil y a un repositorio de materiales bibliográficos en formato electrónico, 
entre otros instrumentos.
</p>
<p>
A medida que avancen los procesos de evaluación, la oferta de recursos se 
ampliará y diversificará a fin de cubrir las necesidades del personal docente 
tanto de Educación Básica como de Educación Media Superior conforme a los 
Perfiles, parámetros e indicadores establecidos para cada función.
</p>
<p>
La Secretaría de Educación Pública confía que este material aporte elementos 
valiosos para mejorar su desempeño en las evaluaciones docentes y en la 
práctica educativa cotidiana.
</p>
