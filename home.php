
<h1>¡BIENVENIDOS!</h1>
<p>
La Secretaría de Educación Pública invita a participar en el Concurso de 
Oposición para el Ingreso a la Educación  Básica, 2015-2016, a:
</p>
<p>
<b>Egresados Escuelas Normales </b>y demás instituciones formadoras de 
docentes de Educación Básica del país, públicas o particulares, con autorización.
</p>
<p>
<b>Egresados de las Instituciones Educativas </b>del país, públicas y 
particulares con reconocimiento de validez oficial, formadoras de los 
profesionales que se 
requieren para los diferentes tipos de evaluación por: nivel educativo, tipo de 
servicio, modalidad, asignatura, tecnología o taller no contempladas en el 
párrafo anterior, a través de Convocatoria Pública y Abierta.
</p>
<p>
Para contar con acciones que fortalezcan los principales procesos de evaluación 
ponemos a su disposición este sitio mediante el cual tendrá acceso libre a la 
información básica sobre los Perfiles, Parámetros e Indicadores de cada figura, 
así como a las Guías de estudio con acceso a la bibliografía de consulta, 
repositorio de materiales, estrategias de estudio, simulador de examen, 
preguntas frecuentes y contacto.
</p>
<p>
<b>¡La SEP confía que este material cumpla con su objetivo para facilitar su 
participación en este proceso!</b>
</p>
